package com.shirsho.parkingfinder.model;

public class ParkingFinderResponseLayout {
	String parkingHouseNumber;
	String parkingStreetName;
	String parkingTownName;
	String parkingStateName;
	public String getParkingHouseNumber() {
		return parkingHouseNumber;
	}
	public void setParkingHouseNumber(String parkingHouseNumber) {
		this.parkingHouseNumber = parkingHouseNumber;
	}
	public String getParkingStreetName() {
		return parkingStreetName;
	}
	public void setParkingStreetName(String parkingStreetName) {
		this.parkingStreetName = parkingStreetName;
	}
	public String getParkingTownName() {
		return parkingTownName;
	}
	public void setParkingTownName(String parkingTownName) {
		this.parkingTownName = parkingTownName;
	}
	public String getParkingStateName() {
		return parkingStateName;
	}
	public void setParkingStateName(String parkingStateName) {
		this.parkingStateName = parkingStateName;
	}
	
	

}
